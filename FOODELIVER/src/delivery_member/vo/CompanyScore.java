package delivery_member.vo;

public class CompanyScore {

	private String company_id;
	private int company_score;
	private int company_count;
	public String getCompany_id() {
		return company_id;
	}
	public void setCompany_id(String company_id) {
		this.company_id = company_id;
	}
	public int getCompany_score() {
		return company_score;
	}
	public void setCompany_score(int company_score) {
		this.company_score = company_score;
	}
	public int getCompany_count() {
		return company_count;
	}
	public void setCompany_count(int company_count) {
		this.company_count = company_count;
	}
	
	
}
